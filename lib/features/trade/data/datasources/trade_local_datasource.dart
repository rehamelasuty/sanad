  // ── Deterministic chart generator ────────────────────────────────────────
  static List<double> _generateChart(double basePrice, int days) {
    final rng = Random(basePrice.toInt());
    final data = <double>[];
    double price = basePrice * 0.92;
    for (var i = 0; i < days; i++) {
      price += (rng.nextDouble() - 0.45) * basePrice * 0.015;
      data.add(price.clamp(basePrice * 0.85, basePrice * 1.15));
    }
    data[data.length - 1] = basePrice;
    return data;
  }

  // ── Fallback generator for unknown symbols ────────────────────────────────
  // Generates a plausible StockDetail for any unrecognized symbol so the
  // TradeScreen never shows an error just because mock data is missing.
  static StockDetail _fallback(String symbol) {
    final isSaudi = RegExp(r'^\d{4}$').hasMatch(symbol);
    final rng = Random(symbol.hashCode.abs());
    final base =
        isSaudi ? 20.0 + rng.nextDouble() * 250 : 15.0 + rng.nextDouble() * 600;
    final change = (rng.nextDouble() - 0.5) * base * 0.04;
    final pct = (change / base) * 100;

    return StockDetail(
      symbol: symbol,
      name: _knownNames[symbol] ?? symbol,
      exchange: isSaudi ? 'تداول' : 'NASDAQ',
      currentPrice: base,
      changeToday: change,
      changeTodayPercent: pct,
      open: base - change * 0.4,
      high: base + base * 0.02,
      low: base - base * 0.02,
      previousClose: base - change,
      volume: (rng.nextInt(5_000_000) + 500_000).toDouble(),
      marketCap: base * (500_000 + rng.nextInt(10_000_000_000)),
      peRatio: 10.0 + rng.nextDouble() * 50,
      week52High: base * 1.35,
      week52Low: base * 0.65,
      isShariaCompliant: rng.nextBool(),
      debtToEquityRatio: rng.nextDouble() * 1.5,
      prohibitedRevenuePercent: rng.nextDouble() * 8,
      purificationPercent: rng.nextDouble() * 0.5,
      logoColor: _fallbackColors[symbol.hashCode.abs() % _fallbackColors.length],
      chartData: _generateChart(base, 14),
    );
  }

