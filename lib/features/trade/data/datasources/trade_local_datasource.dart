  // ── Public API ─────────────────────────────────────────────────────────────

  @override
  Future<StockDetail> getStockDetail(String symbol) async {
    await Future<void>.delayed(const Duration(milliseconds: 600));
    return _stocks[symbol.toUpperCase()] ?? _fallback(symbol);
  }

  @override
  Future<Order> placeOrder({
    required String symbol,
    required OrderSide side,
    required OrderType type,
    required double quantity,
    double? limitPrice,
  }) async {
    await Future<void>.delayed(const Duration(milliseconds: 800));
    final stock = _stocks[symbol.toUpperCase()];
    final execPrice = limitPrice ?? stock?.currentPrice ?? 100.0;
    return Order(
      id: 'ORD-${DateTime.now().millisecondsSinceEpoch}',
      symbol: symbol,
      side: side,
      type: type,
      quantity: quantity,
      limitPrice: execPrice,
      status: OrderStatus.filled,
      createdAt: DateTime.now(),
      filledPrice: execPrice,
      filledAt: DateTime.now(),
    );
  }

  @override
  Future<List<Order>> getOrderHistory(String symbol) async {
    await Future<void>.delayed(const Duration(milliseconds: 400));
    return [];
  }
